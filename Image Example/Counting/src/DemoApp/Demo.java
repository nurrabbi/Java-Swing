    private void AutoID() {
        ///////// start of auto increment
        try {
            String id;
            String query5 = "select COALESCE(MAX(Sid),0) from student";
            PreparedStatement st = connection.prepareStatement(query5);

            ResultSet res5 = st.executeQuery();
            while (res5.next()) {
                id = res5.getString("COALESCE(MAX(Sid),0)");
                int tt = Integer.parseInt(id);
                int ss1 = tt + 1;
                String ss5 = Integer.toString(ss1);
                txtID.setText(ss5);//Sales no
                //  labelClassId.setEditable(false);//sales no
                System.out.println(ss5);
            }
        } catch (SQLException | NumberFormatException ex1) {
            JOptionPane.showMessageDialog(null, ex1.toString());
        }//end try-catch

        /////////////// end of auto increment
    }

