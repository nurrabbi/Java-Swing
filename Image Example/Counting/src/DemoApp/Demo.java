    // bind a list of item from mysql database
    public List<Item> getItemsList() {
        try {
            // Connection connection = getConnection();

            // Statement st = connection.createStatement();
            String qry = "SELECT * FROM student";

            PreparedStatement st = connection.prepareStatement(qry);

            ResultSet res5 = st.executeQuery();

            List<Item> list = new ArrayList<Item>();

            Item item;

            while (res5.next()) {
                item = new Item(res5.getString("Sid"), res5.getString("name"),
                        res5.getBytes("picture"));

                list.add(item);
            }
            return list;

        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

