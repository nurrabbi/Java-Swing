    public void Adddate() {

        if ("".equals(txtDays.getText())) {
            JOptionPane.showMessageDialog(this, "Type the amount of Days to Calculate...!!!");
        } else {
            int d = Integer.parseInt(txtDays.getText());

            Calendar c = Calendar.getInstance();
            c.setTime(new Date()); // Using today's date
            c.add(Calendar.DATE, d); // Adding 5 days
            String output = SDF.format(c.getTime());
//        System.out.println(output);
            txtResult.setText(output);
        }
    }

    public void Subdate() {
        if ("".equals(txtDays.getText())) {
            JOptionPane.showMessageDialog(this, "Type the amount of Days to Calculate...!!!");
        } else {
            int d = Integer.parseInt(txtDays.getText());

            Calendar c = Calendar.getInstance();
            c.setTime(new Date()); // Using today's date
            c.add(Calendar.DATE, -d); // Adding 5 days

            String output = SDF.format(c.getTime());
//        System.out.println(output);
            txtResult.setText(output);
        }
    }

