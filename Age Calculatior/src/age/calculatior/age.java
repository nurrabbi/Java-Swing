    public void DateConvertion() {

        Date date = new Date();
        SimpleDateFormat SDF = new SimpleDateFormat("dd/MM/yyyy");
        String strDate = SDF.format(date);
//        System.out.println("Date Format with dd/MM/yyyy : " + strDate);

        String ar[] = strDate.split("/");

        String D = ar[0];
        String M = ar[1];
        String Y = ar[2];

        ComboDateN.setSelectedItem(D);
        ComboMonthN.setSelectedItem(M);
        ComboYearN.setSelectedItem(Y);

    }

    public void AgeCal() {

        int d = Integer.parseInt(ComboDate.getSelectedItem().toString());
        int m = Integer.parseInt(ComboMonth.getSelectedItem().toString());
        int y = Integer.parseInt(ComboYear.getSelectedItem().toString());
        LocalDate d1 = LocalDate.of(y, m, d);

        int dN = Integer.parseInt(ComboDateN.getSelectedItem().toString());
        int mN = Integer.parseInt(ComboMonthN.getSelectedItem().toString());
        int yN = Integer.parseInt(ComboYearN.getSelectedItem().toString());
        LocalDate d2 = LocalDate.of(yN, mN, dN);

        Period p = Period.between(d1, d2);

//        System.out.println("Your age is: " + p.getYears() + " Years " + p.getMonths() + " Months " + p.getDays() + " Days.");
        txtAge.setText(p.getYears() + " Years " + p.getMonths() + " Months " + p.getDays() + " Days.");
    }

