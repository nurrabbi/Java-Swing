    private void jButtonStuViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStuViewActionPerformed
        try {
            // "View" Button (Tab: Student Information)
            DefaultTableModel modelStudent = (DefaultTableModel) jTableStuInfo.getModel();
            String str = "SELECT StuID,StuName,Class,Section,Major FROM student_info";
            PreparedStatement Sta = DBCon.prepareStatement(str);

            ResultSet resultQuery = Sta.executeQuery();
            while (resultQuery.next()) {
                String ID = resultQuery.getString("StuID");
                String NAME = resultQuery.getString("StuName");
                String CLS = resultQuery.getString("Class");
                String SEC = resultQuery.getString("Section");
                String MAJ = resultQuery.getString("Major");
                modelStudent.addRow(new Object[]{ID, NAME, CLS, SEC, MAJ});
            }
        } catch (SQLException ex) {
            Logger.getLogger(IndexFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonStuViewActionPerformed

