    private void jButtonBookViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBookViewActionPerformed
        try {
            // "View" Button (Book Information)
            DefaultTableModel modelBook = (DefaultTableModel) jTableBookInfo.getModel();
            String str = "SELECT * FROM book_info";
            PreparedStatement PS = DBCon.prepareStatement(str);
            ResultSet RS = PS.executeQuery();
            while (RS.next()) {
                modelBook.addRow(new Object[]{RS.getString("BookID"), RS.getString("BookTitle"), RS.getString("Class"), RS.getString("Edition"), RS.getString("Author"), RS.getString("Publication"), RS.getString("ISBN")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(IndexFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonBookViewActionPerformed

